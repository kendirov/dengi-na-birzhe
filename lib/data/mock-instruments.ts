/** Full mock watchlist for development — always labeled as demo in UI */
export function getMockRawInstruments(): MarketInstrumentRaw[] {
  return rawInstruments.map((inst) => ({
    ...inst,
    bid: inst.spreadRub !== null && inst.spreadRub > 0 ? inst.price - inst.spreadRub / 2 : null,
    ask: inst.spreadRub !== null && inst.spreadRub > 0 ? inst.price + inst.spreadRub / 2 : null,
    volume: null,
    dayHigh: null,
    dayLow: null,
    baselineStatus: "live_current" as const,
  }));
}

/** @deprecated use getMockRawInstruments */
export const mockInstruments = getMockRawInstruments();

export function getMockInstrument(ticker: string): MarketInstrumentRaw | null {
  return getMockRawInstruments().find((i) => i.ticker === ticker) ?? null;
}
